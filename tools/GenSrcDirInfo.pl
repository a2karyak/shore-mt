open INFILE, "<$input" or die "open $input";
open OUTFILE, ">$output" or die "open $output";

print OUTFILE $header;

while (<INFILE>)  {
    next if (/^\s*(#.*)?$/);

    if (/^\s*(\S*)\s*(.*)/)  {
	my ($name, $path) = ($1, $2);
	print OUTFILE <<EOF;

${name}_PATH           = $path
${name}_DIR            = \$(TOP)/\$(${name}_PATH)
${name}_BUILD_DIR      = \$(BUILD_TOP)/\$(${name}_PATH)
${name}_LIB            = \$(${name}_BUILD_DIR)/lib$name.LIBEXT
${name}_SHARED_LIB     = \$(${name}_BUILD_DIR)/lib$name.DLLEXT
${name}_INCL_DIR       = \$(${name}_DIR)
${name}_INCL_BUILD_DIR = \$(${name}_BUILD_DIR)
EOF

        print OUTFILE "${name}_INCL           = -I\$(${name}_INCL_DIR)";
	print OUTFILE " -I\$(${name}_INCL_BUILD_DIR)" if $useBuildDir;
	print OUTFILE "\n";
    }  else  {
	die;
    }
}

