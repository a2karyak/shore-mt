use strict;
use Getopt::Long;

sub Usage
{
    my $progname = $0;
    $progname =~ s/.*[\\\/]//;
    print STDERR <<EOF;
Usage: $progname [--input=infilename] [--output=outfilename] [--createDirs] [--help]
Generates set of make variables from list of names and directories.

    --input         input file
    --ouput         output file
    --createDirs    creates dir for output file if it doesn't exist
    --help|h        prints this message and exits.
EOF
}

my %options = (input => '-', output => '-', createDirs => 0, help => 0);
my @options = ("input=s", "output=s", "createDirs!", "help|h!");
my $ok = GetOptions(\%options, @options);
$ok = 0 if $#ARGV != -1;
if (!$ok || $options{help})  {
    Usage();
    exit($ok == 0);
}


sub CreateDirs
{
    my $dir = shift;

    return if -e $dir;

    my $parentDir;
    ($parentDir) = ($dir =~ /(.*)\//);
    $parentDir = '.' if !$parentDir;

    CreateDirs($parentDir) unless -e $parentDir;

    my $mode = (stat $parentDir)[2] | 0700;
    mkdir $dir, $mode or die "mkdir $dir";
}


sub CanonicalFilename
{
    my $filename = shift;
    my @filename = split(/\//, $filename);
    foreach my $i (reverse 0 .. $#filename)  {
        splice(@filename, $i, 1) if $filename[$i] eq '.' || $i != 0 && $filename[$i] eq '';
    }
    my $i = 1;
    while ($i <= $#filename)  {
        if ($filename[$i] eq '..' && $filename[$i - 1] ne '..')  {
            splice(@filename, $i - 1, 2);
            --$i if $i > 1;
        }  else  {
            ++$i;
        }
    }
    $filename[0] = '.' if $#filename == -1;
    join('/', @filename);
}

my $input = $options{input};
my $output = $options{output};
my $input_dir = '-';
my $output_dir = '-';
$input_dir  = CanonicalFilename($input =~ /(.*)\//) if $input ne '-';
$output_dir = CanonicalFilename($output =~ /(.*)\//) if $output ne '-';
my $useBuildDir = ($input_dir ne $output_dir);
my $curTime = localtime;

CreateDirs($output =~ /(.*)\//) if defined $options{createDirs} && $output ne '-';


